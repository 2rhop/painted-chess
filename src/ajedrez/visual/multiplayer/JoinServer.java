    void join() {
        MultiJugador p = Util.multijugador;
        DatosConexion data = p.dc;
        if (!host.getText().equals("") && !port.getText().equals("")
                && !user.getText().equals("") && !bd.getText().equals("")) {
            if (Util.validarIP(host.getText())) {
                miConexion = new ConexionJDBC(host.getText(), port.getText(), bd.getText(), user.getText(), pass.getText());

                // conecto con la bd postgres
                if (miConexion.conectarJDBC3()) {

                    try {
                        UIManager.setLookAndFeel(new WindowsLookAndFeel());
                        try {

//                            System.out.println(rs1.getString(0));
                            ResultSet rs1 = miConexion.ejecutarConsulta("SELECT * FROM conection WHERE id='1'");
                            Object[][] tab = DatosConexion.getTableFomResultSet(rs1);

                            String serv = (String) tab[0][0];
                            if (!serv.equals(data.invitado)) {
                                ResultSet rs2 = miConexion.ejecutarConsulta("UPDATE conection SET invitado='" + data.invitado + "'");
                                if (p != null) {
                                    p.dispose();
                                }
                                dispose();
                                new WaitingRoom(data, miConexion);

                            } else {
                                Util.showError(Util.idioma[115]);
                                dispose();
                            }

                        } catch (Exception e) {
                            try {
                                ResultSet rs2 = miConexion.ejecutarConsulta("UPDATE conection SET invitado='" + "" + "'");
                            } catch (SQLException ex) {
                                Logger.getLogger(JoinServer.class.getName()).log(Level.SEVERE, null, ex);
                            }
                            Util.showError(Util.idioma[107]);

                            this.dispose();
                        }
                    } catch (UnsupportedLookAndFeelException ex) {
                        Util.showError(ex.toString());
                    }
                } else {
                    Util.showError(miConexion.conectarJDBC1(), Util.idioma[101]);
                }

            } else {
                Util.showError(Util.idioma[95]);
            }
        } else {
            Util.showWarning(Util.idioma[13]);
        }

    }

